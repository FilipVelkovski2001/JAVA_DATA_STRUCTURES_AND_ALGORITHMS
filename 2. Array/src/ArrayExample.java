package Arrays;

import java.util.Arrays;

public class ArrayExample {

	public static void main(String[] args) {
		int[] nums = new int[3]; // Initializing array
		nums[0] = 10; // Giving values to the array index
		nums[1] = 20;
		nums[2] = 30;
		System.out.println(nums.length); // Getting the lenght of the array
		System.out.println(Arrays.toString(nums)); // Printing the array using the toString method
	}
	
	
}
